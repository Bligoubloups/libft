char	*ft_str_toupper(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (!('a' <= str[i] && str[i] <= 'z'))
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
